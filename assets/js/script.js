//creacion de variables y entrada de datos
var num1 = prompt("Ingrese el primer número");
var num2 = prompt("Ingrese el segundo número");
//procesameinto de datos
var suma = parseInt(num1)+parseInt(num2);
var resta = parseInt(num1)-parseInt(num2);
var multiplica = parseInt(num1)*parseInt(num2);
var divide = parseInt(num1)/parseInt(num2);
var modulo = parseInt(num1)%parseInt(num2);
//vista de los resultados del procesamiento
alert(`el resultado de sumar es ${suma}.`);
alert(`el resultado de restar es ${resta}.`);
alert(`el resultado de multiplicar es ${multiplica}.`);
alert(`el resultado de dividir es ${divide}.`);
alert(`el resultado de modulo es ${modulo}.`);









